import type { BibleBook, CanonDivision } from "@/domain/content/types";

type TestamentLabel = "Old Testament" | "New Testament" | "Deuterocanon";

export type CanonEntry = {
  slug: string;
  name: string;
  shortName: string;
  canonDivision: CanonDivision;
  testamentLabel: TestamentLabel;
  chapterCount: number;
  order: number;
  osisCodes: string[];
  aliases: string[];
  rsvBookNumber?: number;
  greekTxtHeaders?: string[];
  brentonTitles?: string[];
  eobTitles?: string[];
  byzFilePrefixes?: string[];
  uanFilePrefixes?: string[];
};

type CanonSeed = Omit<CanonEntry, "aliases" | "osisCodes"> & {
  aliases?: string[];
  osisCodes?: string[];
};

const ORDINAL_WORDS = ["", "First", "Second", "Third", "Fourth"] as const;
const ORDINAL_ROMAN = ["", "I", "II", "III", "IV"] as const;

function normalizeAlias(value: string) {
  return value
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/gi, " ")
    .trim()
    .replace(/\s+/g, " ")
    .toLowerCase();
}

function unique(values: Array<string | undefined>) {
  return Array.from(
    new Set(values.filter((value): value is string => Boolean(value)).map((value) => value.trim())),
  );
}

function ordinalAliases(number: 1 | 2 | 3 | 4, title: string, shortTitle?: string) {
  const word = ORDINAL_WORDS[number];
  const roman = ORDINAL_ROMAN[number];

  return unique([
    `${number} ${title}`,
    `${roman} ${title}`,
    `${word} ${title}`,
    shortTitle ? `${number} ${shortTitle}` : undefined,
    shortTitle ? `${roman} ${shortTitle}` : undefined,
    shortTitle ? `${word} ${shortTitle}` : undefined,
  ]);
}

function book(seed: CanonSeed): CanonEntry {
  return {
    ...seed,
    osisCodes: unique([seed.slug, ...(seed.osisCodes ?? [])]),
    aliases: unique([seed.name, seed.shortName, ...(seed.aliases ?? [])]),
  };
}

