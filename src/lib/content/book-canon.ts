export const FULL_CANON = [...OT_BOOKS, ...DEUTEROCANON_BOOKS, ...NT_BOOKS];

const bySlug = new Map<string, CanonEntry>();
const byOsisCode = new Map<string, CanonEntry>();
const byRsvBookNumber = new Map<number, CanonEntry>();
const byAlias = new Map<string, CanonEntry>();
const byGreekHeader = new Map<string, CanonEntry>();
const byBrentonTitle = new Map<string, CanonEntry>();
const byEobTitle = new Map<string, CanonEntry>();
const byByzPrefix = new Map<string, CanonEntry>();
const byUanPrefix = new Map<string, CanonEntry>();

function registerAliases(map: Map<string, CanonEntry>, entry: CanonEntry, values: string[]) {
  for (const value of values) {
    map.set(normalizeAlias(value), entry);
  }
}

for (const entry of FULL_CANON) {
  bySlug.set(entry.slug, entry);

  for (const osisCode of entry.osisCodes) {
    byOsisCode.set(osisCode, entry);
  }

  if (typeof entry.rsvBookNumber === "number") {
    byRsvBookNumber.set(entry.rsvBookNumber, entry);
  }

  registerAliases(byAlias, entry, entry.aliases);
  registerAliases(byGreekHeader, entry, entry.greekTxtHeaders ?? []);
  registerAliases(byBrentonTitle, entry, entry.brentonTitles ?? []);
  registerAliases(byEobTitle, entry, entry.eobTitles ?? []);

  for (const prefix of entry.byzFilePrefixes ?? []) {
    byByzPrefix.set(prefix.toUpperCase(), entry);
  }

  for (const prefix of entry.uanFilePrefixes ?? []) {
    byUanPrefix.set(prefix.toUpperCase(), entry);
  }
}

export function canonEntryToBook(entry: CanonEntry): BibleBook {
  return {
    id: entry.slug,
    slug: entry.slug,
    name: entry.name,
    shortName: entry.shortName,
    canonDivision: entry.canonDivision,
    testamentLabel: entry.testamentLabel,
    chapterCount: entry.chapterCount,
    order: entry.order,
  };
}

export function getAllBooks() {
  return FULL_CANON.map(canonEntryToBook);
}

export function getCanonBySlug(slug: string) {
  return bySlug.get(slug);
}

export function getCanonByOsisCode(code: string) {
  return byOsisCode.get(code);
}

export function getCanonByRsvBookNumber(bookNumber: number) {
  return byRsvBookNumber.get(bookNumber);
}

export function getCanonByAlias(alias: string) {
  return byAlias.get(normalizeAlias(alias));
}

export function getCanonByGreekHeader(header: string) {
  return byGreekHeader.get(normalizeAlias(header));
}

export function getCanonByBrentonTitle(title: string) {
  return byBrentonTitle.get(normalizeAlias(title));
}

export function getCanonByEobTitle(title: string) {
  return byEobTitle.get(normalizeAlias(title));
}

export function getCanonByByzPrefix(prefix: string) {
  return byByzPrefix.get(prefix.toUpperCase());
}

export function getCanonByUanPrefix(prefix: string) {
  return byUanPrefix.get(prefix.toUpperCase());
}

export function getReferenceBookAliases() {
  return FULL_CANON.map((entry) => ({
    slug: entry.slug,
    name: entry.name,
    aliases: entry.aliases.map(normalizeAlias),
  }));
}

export function normalizeCanonAlias(value: string) {
  return normalizeAlias(value);
}
